public class reales {
    public static void main(String[] args) {
        //tipo float
        float numeroFloat=3.256e12f;
        System.out.println("numeroFloat = " + numeroFloat);
        System.out.println("En Bytes son " + Float.BYTES);
        System.out.println("En Bits son " + Float.SIZE);
        System.out.println("En max son " + Float.MAX_VALUE);
        System.out.println("En min son " + Float.MIN_VALUE);
        System.out.println("max exp " + Float.MAX_EXPONENT);
        System.out.println("max exp " + Float.MIN_EXPONENT);
        System.out.println("inf pos " + Float.POSITIVE_INFINITY);
        System.out.println("inf neg " + Float.NEGATIVE_INFINITY);

        //tipo double
        double numeroDouble=5.656e134d;
        System.out.println("numeroDouble = " + numeroDouble);
        System.out.println("En Bytes son " + Double.BYTES);
        System.out.println("En Bits son " + Double.SIZE);
        System.out.println("En max son " + Double.MAX_VALUE);
        System.out.println("En min son " + Double.MIN_VALUE);
        System.out.println("max exp " + Double.MAX_EXPONENT);
        System.out.println("max exp " + Double.MIN_EXPONENT);
        System.out.println("inf pos " + Double.POSITIVE_INFINITY);
        System.out.println("inf neg " + Double.NEGATIVE_INFINITY);

    }
}
